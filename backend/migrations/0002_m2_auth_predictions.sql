-- M2 schema: users, predictions, points, bonus rules, tournament picks, audit.
-- See docs/adr/0005 (auth), 0008 (scoring), 0009 (audit). Applied idempotently
-- on boot inside a transaction. Does NOT alter teams/matches.

CREATE TABLE IF NOT EXISTS users (
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    google_sub         TEXT UNIQUE,
    email              TEXT,
    nickname           TEXT        NOT NULL UNIQUE,
    avatar_url         TEXT,
    favorite_team_code TEXT,
    telegram_chat_id   TEXT,
    role               TEXT        NOT NULL DEFAULT 'player' CHECK (role IN ('player', 'admin')),
    approved           BOOLEAN     NOT NULL DEFAULT TRUE,
    created_at         TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS predictions (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id             BIGINT      NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    match_id            BIGINT      NOT NULL REFERENCES matches (id) ON DELETE CASCADE,
    home_pred           INTEGER     NOT NULL,
    away_pred           INTEGER     NOT NULL,
    winner_pick_team_id BIGINT      REFERENCES teams (id),
    created_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at          TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (user_id, match_id)
);

CREATE INDEX IF NOT EXISTS idx_predictions_match ON predictions (match_id);

CREATE TABLE IF NOT EXISTS points (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id        BIGINT  NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    match_id       BIGINT  NOT NULL REFERENCES matches (id) ON DELETE CASCADE,
    points         INTEGER NOT NULL DEFAULT 0,
    breakdown_json JSONB,
    UNIQUE (user_id, match_id)
);

CREATE INDEX IF NOT EXISTS idx_points_user ON points (user_id);

CREATE TABLE IF NOT EXISTS bonus_rules (
    id      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    kind    TEXT    NOT NULL UNIQUE,
    enabled BOOLEAN NOT NULL DEFAULT FALSE,
    pts     INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS champion_tiers (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    window_end TIMESTAMPTZ,
    pts        INTEGER NOT NULL DEFAULT 0
);

CREATE TABLE IF NOT EXISTS tournament_picks (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id     BIGINT      NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    kind        TEXT        NOT NULL,
    pick_ref    TEXT        NOT NULL,
    locked_at   TIMESTAMPTZ,
    tier_points INTEGER,
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    UNIQUE (user_id, kind)
);

CREATE TABLE IF NOT EXISTS audit_log (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    actor_user_id  BIGINT      REFERENCES users (id) ON DELETE SET NULL,
    actor_role     TEXT,
    action         TEXT        NOT NULL,
    match_id       BIGINT      REFERENCES matches (id) ON DELETE SET NULL,
    target_user_id BIGINT      REFERENCES users (id) ON DELETE SET NULL,
    created_at     TIMESTAMPTZ NOT NULL DEFAULT now()
);

CREATE INDEX IF NOT EXISTS idx_audit_created_at ON audit_log (created_at DESC);
